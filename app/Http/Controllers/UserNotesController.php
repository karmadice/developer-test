    {
        $userNote = new UserNote();
        $userNote->user_id = $request->input('user_id');
        $userNote->body = trim($request->input('note'));
        $userNote->save();

        return response()->json([
            'note_id' => $userNote->id,
            'created_at' => DATE('Y-m-d h:i:s'),
        ]);
    }

